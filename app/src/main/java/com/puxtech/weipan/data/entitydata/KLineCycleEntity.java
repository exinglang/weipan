package com.puxtech.weipan.data.entitydata;


public class KLineCycleEntity {

	int id;
	String name;
	String mark;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMark() {
		return mark;
	}

	public void setMark(String mark) {
		this.mark = mark;
	}

	@Override
	public String toString() {
		return "KLineCycleEntity [id=" + id + ", name=" + name + ", mark="
				+ mark + "]";
	}

}
