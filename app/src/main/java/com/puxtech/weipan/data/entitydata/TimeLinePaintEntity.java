package com.puxtech.weipan.data.entitydata;


import android.graphics.Paint;

public class TimeLinePaintEntity {

	Paint linePaint;// 边框
	Paint linePaintDashed;// 边框虚线
	Paint redTextPaint;// 红字
	Paint greenTextPaint;// 绿字
	Paint whiteTextPaint;// 白字
	Paint pricePaint;// 价格线
	Paint priceFillPaint;// 价格填充颜色
	Paint averagePricePaint;// 平均价格线

	public Paint getLinePaint() {
		return linePaint;
	}

	public void setLinePaint(Paint linePaint) {
		this.linePaint = linePaint;
	}

	public Paint getLinePaintDashed() {
		return linePaintDashed;
	}

	public void setLinePaintDashed(Paint linePaintDashed) {
		this.linePaintDashed = linePaintDashed;
	}

	public Paint getRedTextPaint() {
		return redTextPaint;
	}

	public void setRedTextPaint(Paint redTextPaint) {
		this.redTextPaint = redTextPaint;
	}

	public Paint getGreenTextPaint() {
		return greenTextPaint;
	}

	public void setGreenTextPaint(Paint greenTextPaint) {
		this.greenTextPaint = greenTextPaint;
	}

	public Paint getWhiteTextPaint() {
		return whiteTextPaint;
	}

	public void setWhiteTextPaint(Paint whiteTextPaint) {
		this.whiteTextPaint = whiteTextPaint;
	}

	public Paint getPricePaint() {
		return pricePaint;
	}

	public void setPricePaint(Paint pricePaint) {
		this.pricePaint = pricePaint;
	}

	public Paint getPriceFillPaint() {
		return priceFillPaint;
	}

	public void setPriceFillPaint(Paint priceFillPaint) {
		this.priceFillPaint = priceFillPaint;
	}

	public Paint getAveragePricePaint() {
		return averagePricePaint;
	}

	public void setAveragePricePaint(Paint averagePricePaint) {
		this.averagePricePaint = averagePricePaint;
	}
}
